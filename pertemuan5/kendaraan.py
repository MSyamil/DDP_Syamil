# membuat list
motorku = [
    "supraX",
    "motor",
    "110cc",
    "hijau",
    2,
]
# cetak list
print(motorku)

# menambahkan harga dan tipe dengan append
motorku.append(7000000) 
motorku.append("gigi")
# cetak list setelah ditambahkan harga dan tipe kendaraan
print(motorku)

# menambahkan jenis kendaraan pada index ke-2 dengan insert
motorku.insert(2, "honda")
# cetak list setelah ditambahkan jenis kendaraan
print(motorku)
